
x = 3
y = 2

# suma
suma = x+y
print(f"Suma: {suma}")

# resta
resta = x-y
print(f"Suma: {resta}")

# multiplicación
mult = x*y
print(f"Suma: {mult}")

# divición
div = x/y
print(f"Suma: {div}")

# divición entero
div = x//y
print(f"Suma: {div}")

# modulo
mod = x%y
print(f"Suma: {mod}")

# exponente
exp = x**y
print(f"Suma: {exp}")




